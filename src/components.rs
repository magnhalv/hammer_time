#[derive(Clone, Copy, Debug, PartialEq)]
pub struct Point {
    pub x: f32,
    pub y: f32
}

#[derive(Clone, Copy, Debug, PartialEq)]
pub struct Player;


#[derive(Clone, Copy, Debug, PartialEq)]
pub struct Gravity;

#[derive(Clone, Copy, Debug, PartialEq)]
pub struct Velocity {
    pub vel_x: f32,
    pub vel_y: f32,
}

